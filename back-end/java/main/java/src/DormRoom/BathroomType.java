package src.DormRoom;

/**
 * Enum of possible bathroom types for a dorm room.
 */
public enum BathroomType {
  /**
   * Indicates the dorm room has its own private bathroom.
   */
  Private,
  /**
   * Indicates the dorm room shares a bathroom with a limited number
   * of other rooms (semi-private).
   */
  SemiPrivate,
  /**
   * Indicates the dorm room shares a large communal bathroom
   */
  Communal
}
